/**
 * Helper Functions for Product Data
 *
 * These utility functions demonstrate common database-like operations.
 */

// Get product by ID
export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

// Get product by slug (for URL routing)
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

// Get all product slugs (useful for static generation in Next.js)
export function getAllProductSlugs(): string[] {
  return products.map(p => p.slug);
}

// Get featured products
export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.featured);
}

// Get products by category
export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category);
}

// Get all unique tags (for filter UI)
export function getAllTags(): string[] {
  const tagSet = new Set<string>();
  products.forEach(p => p.tags.forEach(tag => tagSet.add(tag)));
  return Array.from(tagSet).sort();
}

// Get price range (for filter UI)
export function getPriceRange(): { min: number; max: number } {
  const prices = products.map(p => p.price);
  return {
    min: Math.min(...prices),
    max: Math.max(...prices),
  };
}
